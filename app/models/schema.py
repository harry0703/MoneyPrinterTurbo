    title_text_color: Optional[str] = "#FFFFFF"
    title_stroke_color: Optional[str] = "#000000"
    title_stroke_width: Optional[float] = 2.0
    title_background_color: Union[bool, str] = "transparent"
    title_position: Optional[str] = "center"
