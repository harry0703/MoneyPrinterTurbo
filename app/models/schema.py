    video_clip_duration: Optional[int] = 5
    video_count: Optional[int] = 1

    video_source: Optional[str] = "pexels"
    video_materials: Optional[List[MaterialInfo]] = (
        None  # Materials used to generate the video
    )
    video_style: Optional[str] = ""  # Global video style keyword
    output_bg_color: Optional[str] = "black"  # Background color for pillarbox/letterbox bars
    
    custom_audio_file: Optional[str] = None  # Custom audio file path, will ignore video_script and disable subtitle
    video_language: Optional[str] = ""  # auto detect
    
    # Multi-scene mode settings
    multi_scene_enabled: Optional[bool] = False  # Enable multi-scene mode
    scenes: Optional[List[dict]] = None  # Scene data for multi-scene mode

    voice_name: Optional[str] = ""
    voice_volume: Optional[float] = 1.0
    voice_rate: Optional[float] = 1.0
    voice_emotion: Optional[str] = ""
    tts_server: Optional[str] = "azure-tts-v1"  # TTS server type (azure-tts-v1, azure-tts-v2, siliconflow, gemini-tts, coze-tts)
    bgm_type: Optional[str] = "random"
    bgm_file: Optional[str] = ""
    bgm_volume: Optional[float] = 0.2

    subtitle_enabled: Optional[bool] = True
    subtitle_position: Optional[str] = "bottom"  # top, bottom, center
